§ 6. Odstąpienie od umowy

1.	Zgodnie z Ustawą z dnia 30 maja 2014r. o prawach konsumenta (Dz. U. 2014 poz. 827) Klient  będący konsumentem ma prawo do pisemnego odstąpienia od zawieranej umowy bez podania przyczyny w terminie czternastu dni od zawarcia umowy. Do zachowania czternastodniowego terminu, o którym mowa w ust. 1, wystarczające jest wysłanie oświadczenia o odstąpieniu przed jego upływem. Oświadczenie można złożyć na formularzu, którego wzór został zamieszczony w regulaminie pod adresem URL https://twojaniedziela.pl/regulamin. Korzystanie z przedmiotowego formularza jest fakultatywne.
2.	Zgodnie z art. 38 ustawy o prawach konsumenta, prawo odstąpienia od umowy zawartej na odległość, zgodnie z ustawą, nie przysługuje konsumentowi w wypadkach:
a)	o świadczenie usług, jeżeli przedsiębiorca wykonał w pełni usługę za wyraźna zgoda konsumenta, który został poinformowany przed rozpoczęciem świadczenia, że po spełnieniu świadczenia przez przedsiębiorcę utraci prawo odstąpienia od umowy; 
b)	zawartej w drodze aukcji publicznej;
c)	o dostarczanie treści cyfrowych, które nie są zapisane na nośniku materialnym, jeżeli spełnianie świadczenia rozpoczęło się za wyraźna zgoda konsumenta przed upływem terminu do odstąpienia od umowy i po poinformowaniu go przez przedsiębiorcę̨ o utracie prawa odstąpienia od umowy.
3.	Administartor informuje Klienta, że natychmiastowe przystąpienie do wykonywania usługi na wyraźne życzenie Klienta przed upływem 14 dni od podpisania niniejszej Umowy będzie:
a)	skutkować utratą prawa do odstąpienia od umowy w przypadku całkowitego wykonania Usługi;
b)	w przypadku częściowego wykonania Usługi, Klient ma obowiązek zapłaty za świadzcenie spełnione do chwili odstąpienia od Umowy. 
4.	W przypadku odstąpienia od niniejszej umowy Klientowi zwracane są wszystkie otrzymane od niego płatności niezwłocznie, a w każdym przypadku nie później niż 14 dni od dnia, w którym Serwis został poinformowany o decyzji Klienta o wykonaniu prawa odstąpienia od umowy z Serwisem.
5.	Zwrotu płatności Serwis dokonuje przy użyciu takiego samego sposobu zapłaty, jakiego użył Klient, chyba, że Klient wyraźnie zgodził się na inny sposób zwrotu, który nie wiążę się dla niego z żadnymi kosztami.  


